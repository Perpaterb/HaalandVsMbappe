function toggledata() {
    const mbappeDiv = document.getElementById("mbappe");
    if (mbappeDiv.style.display === "none") {
        mbappeDiv.style.display = "block";
    } else {
        mbappeDiv.style.display = "none";
    }
    const haalandDiv = document.getElementById("haaland");
    if (haalandDiv.style.display === "none") {
        haalandDiv.style.display = "block";
    } else {
        haalandDiv.style.display = "none";
    }
  }